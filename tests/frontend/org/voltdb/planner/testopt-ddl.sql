CREATE TABLE TABLEA (
   A_ID 		BIGINT NOT NULL,
   A_VALUE0 	VARCHAR(64),
   A_VALUE1 	VARCHAR(64),
   A_VALUE2 	VARCHAR(64),
   A_VALUE3 	VARCHAR(64),
   A_VALUE4 	VARCHAR(64),
   A_VALUE5 	VARCHAR(64),
