CREATE TABLE TABLEB (
   B_ID 		BIGINT NOT NULL,
   B_A_ID 		BIGINT NOT NULL REFERENCES TABLEA (A_ID),
   B_VALUE0 	VARCHAR(64),
   B_VALUE1 	VARCHAR(64),
   B_VALUE2 	VARCHAR(64),
   B_VALUE3 	VARCHAR(64),
   B_VALUE4 	VARCHAR(64),
   B_VALUE5 	VARCHAR(64),
